// CCCalcDlg 消息处理程序

BOOL CCCalcDlg::OnInitDialog()
{
	CDialog::OnInitDialog();

	// 将\“关于...\”菜单项添加到系统菜单中。

	// IDM_ABOUTBOX 必须在系统命令范围内。
	ASSERT((IDM_ABOUTBOX & 0xFFF0) == IDM_ABOUTBOX);
	ASSERT(IDM_ABOUTBOX < 0xF000);

	CMenu* pSysMenu = GetSystemMenu(FALSE);
	if (pSysMenu != NULL)
	{
		CString strAboutMenu;
		strAboutMenu.LoadString(IDS_ABOUTBOX);
		if (!strAboutMenu.IsEmpty())
		{
			pSysMenu->AppendMenu(MF_SEPARATOR);
			pSysMenu->AppendMenu(MF_STRING, IDM_ABOUTBOX, strAboutMenu);
		}
	}

	// 设置此对话框的图标。当应用程序主窗口不是对话框时，框架将自动
	//  执行此操作
	SetIcon(m_hIcon, TRUE);			// 设置大图标
	SetIcon(m_hIcon, FALSE);		// 设置小图标

	// TODO: 在此添加额外的初始化代码
	m_edit_02.SetWindowText("此计算器!支持简单的算术表达式,支持括号嵌套(最大128层)!!"); 

	
	return TRUE;  // 除非设置了控件的焦点，否则返回 TRUE
}

void CCCalcDlg::OnSysCommand(UINT nID, LPARAM lParam)
{
	if ((nID & 0xFFF0) == IDM_ABOUTBOX)
	{
		CAboutDlg dlgAbout;
		dlgAbout.DoModal();
	}
	else
	{
		CDialog::OnSysCommand(nID, lParam);
	}
}

// 如果向对话框添加最小化按钮，则需要下面的代码
//  来绘制该图标。对于使用文档/视图模型的 MFC 应用程序，
//  这将由框架自动完成。

void CCCalcDlg::OnPaint() 
{
	if (IsIconic())
	{
		CPaintDC dc(this); // 用于绘制的设备上下文

		SendMessage(WM_ICONERASEBKGND, reinterpret_cast<WPARAM>(dc.GetSafeHdc()), 0);

		// 使图标在工作矩形中居中
		int cxIcon = GetSystemMetrics(SM_CXICON);
		int cyIcon = GetSystemMetrics(SM_CYICON);
		CRect rect;
		GetClientRect(&rect);
		int x = (rect.Width() - cxIcon + 1) / 2;
		int y = (rect.Height() - cyIcon + 1) / 2;

		// 绘制图标
		dc.DrawIcon(x, y, m_hIcon);
	}
	else
	{
		CDialog::OnPaint();
	}
}

//当用户拖动最小化窗口时系统调用此函数取得光标显示。
HCURSOR CCCalcDlg::OnQueryDragIcon()
{
	return static_cast<HCURSOR>(m_hIcon);
}




void CCCalcDlg::OnBnClickedRun()
{
	// TODO: 在此添加控件通知处理程序代码

#define CODE_LEN	512		//表达式最大长度

	char mBuf[CODE_LEN] = {0};

	int mLen = m_edit_01.GetWindowTextLength();	//得到表达式长度

	if ( mLen++ > 511 )
	{
		m_edit_02.SetWindowText("error: 表达式过长!!");
	
		return;
	}


	m_edit_01.GetWindowText( LPTSTR(mBuf) , mLen ); 

	CCalc* mCalc = new CCalc();	//生成实例

	mCalc->Scan(mBuf);

	if ( !mCalc->GetError() )
	{			
		if ( mCalc->DeCoder() )
		{
			
			::gcvt(mCalc->mValue,19,mBuf);

			m_edit_02.SetWindowText(LPCTSTR(mBuf));

		}
		else goto Err_Addr;
	}
	else
	{
		//ERROR MESSAGE
Err_Addr:

		m_edit_02.SetWindowText("");

		for ( int i = 0 ; i < mCalc->mTo ; i++ )
		{
			m_edit_02.ReplaceSel(mCalc->mMsg[i]);
		}
		
	}

	delete mCalc;
}

